
HTTP
Hyper Text Transfer Protocol
-foundation of any data exchange on the Web.
-the protocol that enables communication between clients (such as web browsers) and servers.

Key features of HTTP include:
1.Stateless Protocol:
     Each request from a client to a server and each response from the server to the client is independent
and not related to previous requests or responses. 
2.Client-Server Model:
     The communication involves a client, which initiates the request, and a server, which processes the 
request and returns the appropriate response
3.Request-Response Format: 
     The client sends an HTTP request to the server, and the server responds with an HTTP response,
4.Text-Based:
     HTTP messages are generally plain text, making it human-readable
5.Connectionless: 
    Each request and response between the client and server is an independent transaction, and the connection 
is closed after the response is delivered. 
6.Statelessness: 
    each request from a client to a server is treated as an independent transaction. To maintain state across 
multiple requests, technologies like cookies or sessions are often employed.


